#Foydalanuvchidan x va y sonlarini olib, x ning y-darajasini konsolga chiqaruvchi funksiya yozing.
def daraja(x,y):
    """Ushbu funksiya foydalanuvchidan x va y sonlarini olib, x ning y-darajasini konsolga chiqaradi."""
    print(f"{x} ning {y}-darajasi: {x**y} ga teng.")
daraja(7, 2)
daraja(7, 3)
daraja(7, 4)
daraja(8,10)